# Початковий список міст
cities_list = ["Київ", "Одеса", "Львів", "Харків", "Житомир"]

# Перетворюємо список на множину для пришвидшення пошуку
cities_set = set(cities_list)

# Перелік міст для перевірки
cities_to_check = ["Одеса", "Полтава"]

# Перевіряємо наявність кожного міста та виводимо повідомлення
for city in cities_to_check:
    if city in cities_set:
        print(f"Місто {city} є у списку.")
    else:
        print(f"Місто {city} відсутнє у списку.")
