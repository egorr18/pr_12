# Словник студентів та їх оцінок
grades = {"Іван": 80, "Марія": 95, "Олег": 78, "Анна": 85}

# Отримання імені студента від користувача
name = input("Введіть ім'я студента: ")

# Пошук оцінки з обробкою винятку
if name in grades:
    print(f"Оцінка студента {name}: {grades[name]}")
else:
    print(f"Студента з ім'ям {name} не знайдено у словнику.")
